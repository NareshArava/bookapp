export class User {
    Username:string;
    Password:string;
    Email:string;
    Firstname:string;
    Lastname:string;
    constructor(Username:string,Password:string,Email:string,Firstname:string,Lastname:string){
        this.Username=Username;
        this.Password=Password;
        this.Email=Email;
        this.Firstname=Firstname;
        this.Lastname=Lastname;
    }


}
